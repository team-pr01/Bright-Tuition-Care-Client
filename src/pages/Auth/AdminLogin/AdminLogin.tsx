/* eslint-disable @typescript-eslint/no-explicit-any */
import { useForm } from "react-hook-form";
import { useNavigate } from "react-router-dom";
import { useDispatch } from "react-redux";
import Cookies from "js-cookie";
import toast from "react-hot-toast";
import { useState } from "react";
import { useLoginMutation } from "../../../redux/Features/Auth/authApi";
import { setUser } from "../../../redux/Features/Auth/authSlice";
import AuthHeading from "../../../components/Reusable/AuthHeading/AuthHeading";
import TextInput from "../../../components/Reusable/TextInput/TextInput";
import PasswordInput from "../../../components/Reusable/PasswordInput/PasswordInput";
import Button from "../../../components/Reusable/Button/Button";
import { ICONS } from "../../../assets";
import ToastMessage from "../../../components/Reusable/ToastMessage/ToastMessage";

type TFormData = {
  email: string;
  password: string;
};
const AdminLogin = () => {
  const [isPasswordVisible, setIsPasswordVisible] = useState<boolean>(false);
  const [login, { isLoading }] = useLoginMutation();
  const {
    register,
    handleSubmit,
    reset,
    formState: { errors },
  } = useForm<TFormData>();
  const dispatch = useDispatch();
  const navigate = useNavigate();
  const handleSigIn = async (data: TFormData) => {
    try {
      const payload = {
        role: "admin",
        email: data.email,
        password: data.password,
      };
      const res = await login(payload).unwrap();
      if (res?.success) {
        toast.custom(() => (
          <ToastMessage
            title="Welcome Back!"
            subTitle="You have successfully signed in."
          />
        ));
        Cookies.set("accessToken", res?.data?.accessToken, {
          expires: 7,
          secure: true,
          sameSite: "strict",
        });
        dispatch(
          setUser({ user: res?.data?.user, token: res?.data?.accessToken })
        );
      }
      if (res?.data?.user?.role === "admin") {
        navigate("/dashboard/admin/home");
      } else if (res?.data?.user?.role === "tutor") {
        navigate("/dashboard/tutor/home");
      } else {
        navigate("/dashboard/guardian/home");
      }
      reset();
    } catch (error: any) {
      toast.error(error?.data?.message || "Login failed. Please try again.");
    }
  };
  return (
    <div className="flex justify-center items-center min-h-screen bg-neutral-50/20 px-4">
      <form
        onSubmit={handleSubmit(handleSigIn)}
        className="w-full max-w-md bg-white/90 backdrop-blur-sm shadow-lg rounded-2xl p-6 md:p-8 flex flex-col gap-6 font-Nunito border border-primary-10/20"
      >
        <AuthHeading
          title="Signin as Admin"
          description="Please enter your email and password to sign in."
        />

        <div className="flex flex-col gap-6">
          {/* Email */}
          <TextInput
            label="Email"
            placeholder="Enter your email"
            type="email"
            error={errors.email}
            {...register("email", {
              required: "Email is required",
              pattern: {
                value: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
                message: "Invalid email address",
              },
            })}
          />

          <PasswordInput
            label="Password"
            placeholder="Must be at least 8 Characters"
            error={errors.password}
            {...register("password", {
              required: "Password is required",
              minLength: {
                value: 8,
                message: "Password must be at least 8 characters",
              },
            })}
            isPasswordVisible={isPasswordVisible}
            setIsPasswordVisible={setIsPasswordVisible}
          />
        </div>

        <div className="flex flex-col gap-3 items-center justify-between">
          <Button
            type="submit"
            label="Sign In"
            variant="primary"
            iconWithoutBg={ICONS.topRightArrowWhite}
            className="py-2 lg:py-2 w-full md:w-fit"
            isLoading={isLoading}
            isDisabled={isLoading}
          />
        </div>
      </form>
    </div>
  );
};

export default AdminLogin;
